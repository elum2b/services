CREATE INDEX IF NOT EXISTS task_progress_missing_reward_snapshot_idx
    ON task_progress (id)
    WHERE rewards_snapshot IS NULL;

-- Preserve the catalog reward visible when legacy progress was first created.
-- New writes populate rewards_snapshot directly.
UPDATE task_progress AS progress
SET rewards_snapshot = COALESCE(
    (
        SELECT jsonb_agg(
            jsonb_strip_nulls(
                jsonb_build_object(
                    'key', reward.reward_key,
                    'type', reward.reward_type,
                    'quantity', reward.quantity,
                    'scale', reward.scale,
                    'unit', reward.duration_unit
                )
            )
            ORDER BY reward.position, reward.id
        )
        FROM task_reward AS reward
        WHERE reward.workspace_id = progress.workspace_id
          AND reward.task_id = progress.task_id
    ),
    '[]'::jsonb
)
WHERE progress.rewards_snapshot IS NULL;


