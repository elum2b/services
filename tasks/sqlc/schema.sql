ALTER TABLE task_partner_issue
    ADD COLUMN IF NOT EXISTS rewards_snapshot JSONB NULL;

UPDATE task_partner_issue AS issue
SET rewards_snapshot = COALESCE(
    (
        SELECT reward_grant.reward_snapshot
        FROM task_partner_reward_grant AS reward_grant
        WHERE reward_grant.workspace_id = issue.workspace_id
          AND reward_grant.issue_id = issue.id
        LIMIT 1
    ),
    (
        SELECT jsonb_agg(
            jsonb_build_object(
                'key', selected.reward_key,
                'type', selected.reward_type,
                'quantity', selected.quantity,
                'scale', selected.scale,
                'unit', selected.duration_unit
            )
            ORDER BY selected.position, selected.reward_key
        )
        FROM (
            SELECT DISTINCT ON (rule.reward_key)
                rule.reward_key,
                rule.reward_type,
                rule.quantity,
                rule.scale,
                rule.duration_unit,
                rule.position
            FROM task_partner_reward_rule AS rule
            WHERE rule.workspace_id = issue.workspace_id
              AND rule.provider = issue.provider
              AND rule.group_key = issue.group_key
              AND rule.external_type IN (issue.external_type, '*')
              AND rule.is_enabled = true
            ORDER BY
                rule.reward_key,
                CASE WHEN rule.external_type = issue.external_type THEN 0 ELSE 1 END,
                rule.position
        ) AS selected
    ),
    '[]'::jsonb
)
WHERE issue.rewards_snapshot IS NULL;

ALTER TABLE task_partner_issue
    ALTER COLUMN rewards_snapshot SET DEFAULT '[]'::jsonb,
    ALTER COLUMN rewards_snapshot SET NOT NULL;


