    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
    workspace_id VARCHAR(36) NOT NULL,
    key VARCHAR(100) NOT NULL,
    group_key VARCHAR(100) NOT NULL,
    sequence_key VARCHAR(100) NULL,
    sequence_position INTEGER NULL,
    task_kind VARCHAR(64) NOT NULL DEFAULT 'internal',
    action_key VARCHAR(150) NOT NULL,
    action_kind VARCHAR(64) NOT NULL,
    claim_mode VARCHAR(64) NOT NULL DEFAULT 'manual',
    start_mode VARCHAR(64) NOT NULL DEFAULT 'none',
    target_count BIGINT NOT NULL DEFAULT 1,
    reset_unit VARCHAR(64) NOT NULL DEFAULT 'never',
    reset_every INTEGER NOT NULL DEFAULT 1,
    position INTEGER NOT NULL DEFAULT 0,
    payload JSONB NULL,
    target JSONB NULL,
    integration_kind VARCHAR(64) NULL,
    integration_provider VARCHAR(64) NULL,
    integration_payload JSONB NULL,
    image_url VARCHAR(1024) NULL,
    is_visible BOOLEAN NOT NULL DEFAULT true,
    is_active BOOLEAN NOT NULL DEFAULT true,
    start_at TIMESTAMPTZ NULL,
    end_at TIMESTAMPTZ NULL,
    deleted_at TIMESTAMPTZ NULL,
    branch_sort_key VARCHAR(101)
        GENERATED ALWAYS AS (COALESCE(sequence_key, '~' || key)) STORED,
    created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    updated_at TIMESTAMPTZ NOT NULL DEFAULT now(),
    PRIMARY KEY (id),
    CONSTRAINT task_definition_workspace_id_uq UNIQUE (workspace_id, id),
